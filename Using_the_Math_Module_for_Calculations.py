num = int(input("Enter a number: "))
import math
def squareroot():
   square_root = math.sqrt(num)
   print("Square root:",square_root)
def logarithim():
    log_result = math.log(num)
    print("Logarithim:",log_result)
def sine():
    sine_value = math.sin(num)
    print("Sine:",sine_value)
squareroot()
logarithim()
sine()